

alert("Apresentar os resultados de uma tabuada de multiplicar (de 1 até 10) de um número qualquer.")



var numero = parseInt(prompt("Digite o valor que deseja saber a tabuada:"))


alert("Tabuada")
for (var contadora = 1; contadora <= 10; contadora++) {
    
   
alert(numero + " X " + contadora + " = " + numero * contadora)
}