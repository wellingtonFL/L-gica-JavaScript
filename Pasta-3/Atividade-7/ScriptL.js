

alert("Elaborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à soma dos quadrados dos três valores lidos.")



alert("Este programa irá afzer a soma dos três numeros ao quadrado")

 let A=parseInt(prompt("Escreva um número que será elevado ao quadrado: "))

 let B=parseInt(prompt("Escreva outro número que será elevado ao quadrado: "))

 let C = parseInt(prompt("Escreva  outro número que será elevado ao quadrado: "))


let quadradoA= A^2
let quadradoB= B^2
let quadradoC= C^2
let somatotal= quadradoA+quadradoB+quadradoC

alert("A soma dos três valores é: "+ somatotal)
