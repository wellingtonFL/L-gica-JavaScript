alert("12) Escreva um algoritmo para ler uma temperatura em graus Fahrenheit, calcular e escrever o valorcorrespondente em graus Celsius (baseado na fórmula abaixo): ")



let Fahrenheit=parseInt(prompt("Escreva a temperatura em Fahrenheit para ser convertida em Celsius: "))


let Celsius = (5 * (Fahrenheit - 32) / 9)

alert("O resultado em Celsius é: " + Celsius)